// MARK: - CockpitBrainError

enum CockpitBrainError: LocalizedError, Sendable {
    case projectPathNotFound(String)
    case invalidPRDJson(String)
    case transverseCategoryNotFound(String)
    case specialistNotFound(String)
    case fileSystemError(String)

    var errorDescription: String? {
        switch self {
        case .projectPathNotFound(let path):
            return "Project path does not exist: \(path)"
        case .invalidPRDJson(let detail):
            return "Invalid PRD JSON: \(detail)"
        case .transverseCategoryNotFound(let name):
            return "No transverse category '\(name)' found in COP"
        case .specialistNotFound(let name):
            return "No specialist '\(name)' found in COP triggers"
        case .fileSystemError(let detail):
            return "File system error: \(detail)"
        }
    }
}

// MARK: - CockpitOrchestrationPlan

/// Cockpit Orchestration Plan generated by analyzing project context and PRD.
struct CockpitOrchestrationPlan: Codable, Sendable {
    let projectName: String
    let projectType: String
    let domain: String
    let marketContext: String
    let transverseProductions: [TransverseCategory]
    let specialistTriggers: [SpecialistTrigger]
    let metaAgentConfig: MetaAgentConfig
    let deliverablesPath: String
    let createdAt: String
}

// MARK: - TransverseCategory

/// A category of transverse deliverables (marketing, documentation, strategy, etc.).
struct TransverseCategory: Codable, Sendable {
    let category: String
    let deliverables: [String]
    let priority: String
}

// MARK: - SpecialistTrigger

/// A trigger condition that activates a specialist agent.
struct SpecialistTrigger: Codable, Sendable {
    let condition: String
    let specialist: String
    let reason: String
}

// MARK: - MetaAgentConfig

/// Configuration for the META monitor agent.
struct MetaAgentConfig: Codable, Sendable {
    let capabilities: [String]
    let monitoringIntervalMs: Int
    let autonomyLevel: String
}

// MARK: - AdaptationAction

/// An action recommended by the adaptation engine based on session state.
struct AdaptationAction: Codable, Sendable {
    let action: String
    let targetSlot: String?
    let reason: String
}

