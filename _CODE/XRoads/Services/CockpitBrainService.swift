    // MARK: - COP Generation

    /// Generate a Cockpit Orchestration Plan by analyzing project context and PRD.
    ///
    /// - Parameters:
    ///   - projectPath: Path to the project directory
    ///   - prdJson: JSON string of the PRD
    /// - Returns: Generated CockpitOrchestrationPlan
    func generateCOP(projectPath: String, prdJson: String) throws -> CockpitOrchestrationPlan {
        let fileManager = FileManager.default
        guard fileManager.fileExists(atPath: projectPath) else {
            throw CockpitBrainError.projectPathNotFound(projectPath)
        }

        guard let prdData = prdJson.data(using: .utf8),
              let prd = try? JSONSerialization.jsonObject(with: prdData) as? [String: Any] else {
            throw CockpitBrainError.invalidPRDJson("Could not parse JSON")
        }

        // Extract project name from path
        let projectName = URL(fileURLWithPath: projectPath).lastPathComponent

        // Detect project characteristics
        let projectType = detectProjectType(projectPath: projectPath)
        let domain = detectDomainFromPRD(prd)
        let marketContext = detectMarketContext(projectPath: projectPath)

        // Build transverse productions
        let transverseProductions = buildTransverseProductions(projectType: projectType, domain: domain)

        // Detect specialist triggers from PRD
        let specialistTriggers = detectSpecialistNeedsFromPRD(prd)

        // Configure META agent
        var capabilities = ["qa", "doc_gen", "git_master"]
        if domain == "security" || specialistTriggers.contains(where: { $0.specialist == "security-auditor" }) {
            capabilities.append("security_scan")
        }

        let metaAgentConfig = MetaAgentConfig(
            capabilities: capabilities,
            monitoringIntervalMs: 30_000,
            autonomyLevel: marketContext == "regulated" ? "limited" : "full"
        )

        let deliverablesPath = "\(projectPath)/.crossroads/deliverables/"

        let cop = CockpitOrchestrationPlan(
            projectName: projectName,
            projectType: projectType,
            domain: domain,
            marketContext: marketContext,
            transverseProductions: transverseProductions,
            specialistTriggers: specialistTriggers,
            metaAgentConfig: metaAgentConfig,
            deliverablesPath: deliverablesPath,
            createdAt: ISO8601DateFormatter().string(from: Date())
        )

        // Persist COP to .crossroads/cockpit-plan.json
        let crossroadsDir = URL(fileURLWithPath: projectPath).appendingPathComponent(".crossroads")
        try fileManager.createDirectory(at: crossroadsDir, withIntermediateDirectories: true)

        let encoder = JSONEncoder()
        encoder.outputFormatting = [.prettyPrinted, .sortedKeys]
        let copData = try encoder.encode(cop)
        try copData.write(to: crossroadsDir.appendingPathComponent("cockpit-plan.json"))

        logger.info("Generated COP for '\(projectName)': type=\(projectType), domain=\(domain)")
        return cop
    }

    // MARK: - COP Loading

    /// Load a previously generated COP from disk.
    ///
    /// - Parameter projectPath: Path to the project directory
    /// - Returns: The COP if one exists on disk, nil otherwise
    func getCOP(projectPath: String) -> CockpitOrchestrationPlan? {
        let copURL = URL(fileURLWithPath: projectPath)
            .appendingPathComponent(".crossroads")
            .appendingPathComponent("cockpit-plan.json")

        guard let data = try? Data(contentsOf: copURL) else { return nil }
        return try? JSONDecoder().decode(CockpitOrchestrationPlan.self, from: data)
    }

