    // MARK: - Deliverables Structure

    /// Create the deliverables directory structure under .crossroads/.
    ///
    /// - Parameters:
    ///   - projectPath: Path to the project directory
    ///   - cop: The Cockpit Orchestration Plan
    func createDeliverablesStructure(projectPath: String, cop: CockpitOrchestrationPlan) throws {
        let fileManager = FileManager.default
        let baseURL = URL(fileURLWithPath: projectPath)
            .appendingPathComponent(".crossroads")
            .appendingPathComponent("deliverables")

        // Standard directories
        let dirs = ["marketing", "research", "documentation", "strategy", "ops", "specialist"]
        for dir in dirs {
            let dirURL = baseURL.appendingPathComponent(dir)
            try fileManager.createDirectory(at: dirURL, withIntermediateDirectories: true)
        }

        // Write a manifest of expected deliverables
        var manifest = "# Deliverables Manifest\n\n"
        manifest += "Project: \(cop.projectName)\n"
        manifest += "Generated: \(cop.createdAt)\n\n"

        for cat in cop.transverseProductions {
            manifest += "## \(capitalizeFirst(cat.category)) (priority: \(cat.priority))\n"
            for d in cat.deliverables {
                manifest += "- [ ] \(d).md\n"
            }
            manifest += "\n"
        }

        if !cop.specialistTriggers.isEmpty {
            manifest += "## Specialist Reports\n"
            for t in cop.specialistTriggers {
                manifest += "- [ ] \(t.specialist)-report.md\n"
            }
            manifest += "\n"
        }

        let manifestURL = baseURL.appendingPathComponent("_manifest.md")
        try manifest.write(to: manifestURL, atomically: true, encoding: .utf8)

        logger.info("Created deliverables structure at \(baseURL.path)")
    }

    // MARK: - Specialist Detection

    /// Detect specialist needs from story titles and descriptions.
    ///
    /// - Parameter stories: Array of (title, description) tuples
    /// - Returns: Array of triggered specialists
    func detectSpecialistNeeds(stories: [(String, String)]) -> [SpecialistTrigger] {
        var textPool = ""
        for (title, description) in stories {
            textPool += title + " " + description + " "
        }

        let lower = textPool.lowercased()
        var triggers: [SpecialistTrigger] = []
        var seenSpecialists: Set<String> = []

        for (keywords, specialist, reason) in Self.specialistRules {
            let matchedKeywords = keywords.filter { lower.contains($0) }

            if !matchedKeywords.isEmpty && !seenSpecialists.contains(specialist) {
                let condition = matchedKeywords
                    .map { "domain contains \"\($0)\"" }
                    .joined(separator: " OR ")

                triggers.append(SpecialistTrigger(
                    condition: condition,
                    specialist: specialist,
                    reason: reason
                ))
                seenSpecialists.insert(specialist)
            }
        }

        return triggers
    }

    // MARK: - Adaptation Actions

    /// Analyze current slot states and session cost to return adaptation actions.
    ///
    /// Rules:
    /// - Error slots > 3: focus debugging
    /// - Individual error slots: target debugging
    /// - Budget > 80%: switch to cheaper model
    /// - All coding complete: activate transverse
    /// - 50%+ coding complete: activate transverse in parallel
    /// - Multiple error slots with similar tasks: pause and resolve conflicts
    /// - No running slots but incomplete: restart stalled
    ///
    /// - Parameters:
    ///   - slots: Current agent slots
    ///   - sessionCost: Current session cost summary
    /// - Returns: Array of recommended adaptation actions
    func getAdaptationActions(slots: [AgentSlot], sessionCost: UsageSummary) -> [AdaptationAction] {
        var actions: [AdaptationAction] = []

        let totalSlots = slots.count
        let doneSlots = slots.filter { $0.status == .done }.count
        let errorSlots = slots.filter { $0.status == .error }
        let runningSlots = slots.filter { $0.status == .running }.count

        // Rule: Too many test failures -> focus debugging
        if errorSlots.count > 3 {
            actions.append(AdaptationAction(
                action: "focus_debugging",
                targetSlot: nil,
                reason: "\(errorSlots.count) slots in error state — prioritize debugging before proceeding"
            ))
        }

        // Rule: Specific error slots -> target them for debugging
        for slot in errorSlots {
            actions.append(AdaptationAction(
                action: "focus_debugging",
                targetSlot: slot.id.uuidString,
                reason: "Slot \(slot.id) (index \(slot.slotIndex)) failed: \(slot.currentTask ?? "unknown error")"
            ))
        }

        // Rule: Budget > 80% -> switch to cheaper model
        let budgetCents = 2500
        let percentUsed = budgetCents > 0
            ? (Double(sessionCost.totalCostCents) / Double(budgetCents)) * 100.0
            : 0.0

        if percentUsed > 80.0 {
            actions.append(AdaptationAction(
                action: "switch_model",
                targetSlot: nil,
                reason: String(format: "Budget at %.0f%% (%dc / %dc) — switch to cheaper model for remaining work",
                               percentUsed, sessionCost.totalCostCents, budgetCents)
            ))
        }

        // Rule: Coding complete -> activate transverse
        if totalSlots > 0 && doneSlots == totalSlots {
            actions.append(AdaptationAction(
                action: "activate_transverse",
                targetSlot: nil,
                reason: "All coding slots completed — activate transverse production agents"
            ))
        } else if totalSlots > 0 {
            let progress = (Double(doneSlots) / Double(totalSlots)) * 100.0
            if progress >= 50.0 {
                actions.append(AdaptationAction(
                    action: "activate_transverse",
                    targetSlot: nil,
                    reason: String(format: "Coding at %.0f%% (%d/%d done) — transverse agents can start in parallel",
                                   progress, doneSlots, totalSlots)
                ))
            }
        }

        // Rule: Conflict detected (multiple error slots with similar tasks)
        if errorSlots.count >= 2 {
            let errorTasks = errorSlots.compactMap(\.currentTask)
            let hasConflict = zip(errorTasks, errorTasks.dropFirst()).contains { pair in
                let wordsA = Set(pair.0.split(separator: " ").map { $0.lowercased() })
                let wordsB = Set(pair.1.split(separator: " ").map { $0.lowercased() })
                return wordsA.intersection(wordsB).count >= 2
            }

            if hasConflict {
                actions.append(AdaptationAction(
                    action: "pause_and_resolve",
                    targetSlot: nil,
                    reason: "Potential conflict detected between error slots — pause and resolve before continuing"
                ))
            }
        }

        // Rule: No running slots but session should be active
        if runningSlots == 0 && doneSlots < totalSlots && totalSlots > 0 {
            actions.append(AdaptationAction(
                action: "restart_stalled",
                targetSlot: nil,
                reason: String(format: "No running slots (%d/%d done) — restart stalled agents",
                               doneSlots, totalSlots)
            ))
        }

        return actions
    }

    // MARK: - Private Helpers

    /// Detect project type from config files in the project directory.
    private func detectProjectType(projectPath: String) -> String {
        let fileManager = FileManager.default
        let packageJsonPath = "\(projectPath)/package.json"
        let cargoTomlPath = "\(projectPath)/Cargo.toml"
        let packageSwiftPath = "\(projectPath)/Package.swift"
        let pubspecPath = "\(projectPath)/pubspec.yaml"
        let gradlePath = "\(projectPath)/build.gradle"
        let gradleKtsPath = "\(projectPath)/build.gradle.kts"

        if fileManager.fileExists(atPath: packageJsonPath) {
            let content = (try? String(contentsOfFile: packageJsonPath, encoding: .utf8)) ?? ""
            if let data = content.data(using: .utf8),
               let pkg = try? JSONSerialization.jsonObject(with: data) as? [String: Any],
               let deps = pkg["dependencies"] as? [String: Any] {

                let hasNext = deps["next"] != nil
                let hasReact = deps["react"] != nil
                let hasExpress = deps["express"] != nil
                let hasFastify = deps["fastify"] != nil

                if hasNext || hasReact { return "saas" }
                if hasExpress || hasFastify { return "api" }
            }

            // Check for CLI indicators
            if let data = content.data(using: .utf8),
               let pkg = try? JSONSerialization.jsonObject(with: data) as? [String: Any],
               pkg["bin"] != nil {
                return "cli"
            }

            return "web"
        }

        if fileManager.fileExists(atPath: cargoTomlPath) {
            let content = ((try? String(contentsOfFile: cargoTomlPath, encoding: .utf8)) ?? "").lowercased()

            if content.contains("[[bin]]") || (content.contains("crate-type") && content.contains("\"bin\"")) {
                return "cli"
            }
            if content.contains("actix") || content.contains("axum") || content.contains("rocket") || content.contains("warp") {
                return "api"
            }
            if content.contains("[lib]") && !content.contains("[[bin]]") {
                return "library"
            }
            return "cli"
        }

        if fileManager.fileExists(atPath: packageSwiftPath) {
            return "cli" // Swift package, default to CLI
        }

        if fileManager.fileExists(atPath: pubspecPath) {
            return "mobile"
        }

        if fileManager.fileExists(atPath: gradlePath) || fileManager.fileExists(atPath: gradleKtsPath) {
            let androidPath = "\(projectPath)/android"
            if fileManager.fileExists(atPath: androidPath) {
                return "mobile"
            }
            return "api"
        }

        return "web"
    }

    /// Detect domain from PRD content using keyword matching.
    private func detectDomainFromPRD(_ prd: [String: Any]) -> String {
        var textPool = ""

        if let name = prd["feature_name"] as? String {
            textPool += name + " "
        }
        if let desc = prd["description"] as? String {
            textPool += desc + " "
        }
        if let stories = prd["user_stories"] as? [[String: Any]] {
            for story in stories {
                if let title = story["title"] as? String {
                    textPool += title + " "
                }
                if let desc = story["description"] as? String {
                    textPool += desc + " "
                }
            }
        }

        let lower = textPool.lowercased()

        let domainRules: [(String, [String])] = [
            ("fintech", ["payment", "billing", "subscription", "invoice", "stripe", "checkout", "wallet", "transaction"]),
            ("health", ["patient", "medical", "health", "diagnosis", "appointment", "prescription", "clinical"]),
            ("security", ["auth", "login", "password", "session", "token", "oauth", "permission", "role", "rbac", "jwt"]),
            ("devtools", ["api", "endpoint", "rest", "graphql", "sdk", "cli", "developer", "plugin", "extension"]),
            ("ecommerce", ["cart", "product", "catalog", "order", "shipping", "inventory"]),
            ("analytics", ["dashboard", "metric", "report", "chart", "analytics", "tracking", "event"]),
            ("data", ["pipeline", "etl", "data", "transform", "migration", "batch", "stream"]),
            ("infra", ["deploy", "infrastructure", "ci", "cd", "kubernetes", "docker", "terraform"]),
            ("ai", ["model", "ml", "prediction", "training", "inference", "embedding", "llm"]),
            ("messaging", ["chat", "message", "notification", "email", "sms", "push"]),
        ]

        var bestDomain = "general"
        var bestScore = 0

        for (domain, keywords) in domainRules {
            let score = keywords.filter { lower.contains($0) }.count
            if score > bestScore {
                bestScore = score
                bestDomain = domain
            }
        }

        return bestDomain
    }

    /// Detect market context from README.md content.
    private func detectMarketContext(projectPath: String) -> String {
        let readmePath = "\(projectPath)/README.md"
        let content = ((try? String(contentsOfFile: readmePath, encoding: .utf8)) ?? "").lowercased()

        if content.contains("compliance") || content.contains("regulation") || content.contains("hipaa")
            || content.contains("gdpr") || content.contains("pci") || content.contains("sox") {
            return "regulated"
        }

        if content.contains("competitor") || content.contains("alternative to")
            || content.contains("vs ") || content.contains("compared to") {
            return "competitive"
        }

        return "greenfield"
    }

    /// Build transverse production categories based on project type and domain.
    private func buildTransverseProductions(projectType: String, domain: String) -> [TransverseCategory] {
        var categories: [TransverseCategory] = []

        // All projects need documentation
        let docDeliverables: [String]
        switch projectType {
        case "cli":
            docDeliverables = ["man-page", "install-guide", "usage-examples", "changelog"]
        case "api":
            docDeliverables = ["api-reference", "sdk-guide", "authentication-guide", "deploy-guide", "changelog"]
        case "library":
            docDeliverables = ["api-reference", "getting-started", "migration-guide", "changelog"]
        default:
            docDeliverables = ["README", "deploy-guide", "architecture-overview", "changelog"]
        }

        categories.append(TransverseCategory(
            category: "documentation",
            deliverables: docDeliverables,
            priority: "high"
        ))

        // SaaS and web projects need marketing
        if projectType == "saas" || projectType == "web" {
            categories.append(TransverseCategory(
                category: "marketing",
                deliverables: ["landing-page-copy", "value-proposition", "feature-comparison", "onboarding-flow-copy"],
                priority: "high"
            ))

            categories.append(TransverseCategory(
                category: "strategy",
                deliverables: ["pricing-model", "go-to-market-plan", "user-persona-profiles"],
                priority: "medium"
            ))
        }

        // API projects need developer-focused research
        if projectType == "api" {
            categories.append(TransverseCategory(
                category: "research",
                deliverables: ["developer-personas", "integration-patterns", "rate-limit-strategy"],
                priority: "medium"
            ))
        }

        // SaaS projects need competitive research
        if projectType == "saas" {
            categories.append(TransverseCategory(
                category: "research",
                deliverables: ["competitive-analysis", "market-sizing", "user-interview-template"],
                priority: "medium"
            ))
        }

        // Domain-specific additions
        switch domain {
        case "fintech":
            categories.append(TransverseCategory(
                category: "ops",
                deliverables: ["compliance-checklist", "audit-trail-spec", "incident-response-plan"],
                priority: "high"
            ))
        case "health":
            categories.append(TransverseCategory(
                category: "ops",
                deliverables: ["hipaa-compliance-checklist", "data-handling-policy", "consent-flow-spec"],
                priority: "high"
            ))
        case "security":
            categories.append(TransverseCategory(
                category: "ops",
                deliverables: ["security-policy", "threat-model", "penetration-test-plan"],
                priority: "high"
            ))
        default:
            break
        }

        return categories
    }

    /// Detect specialist needs directly from a parsed PRD dictionary.
    private func detectSpecialistNeedsFromPRD(_ prd: [String: Any]) -> [SpecialistTrigger] {
        guard let stories = prd["user_stories"] as? [[String: Any]] else { return [] }

        let storyPairs: [(String, String)] = stories.map { story in
            let title = (story["title"] as? String) ?? ""
            let desc = (story["description"] as? String) ?? ""
            return (title, desc)
        }

        return detectSpecialistNeeds(stories: storyPairs)
    }

    /// Capitalize the first letter of a string.
    private func capitalizeFirst(_ s: String) -> String {
        guard let first = s.first else { return "" }
        return first.uppercased() + s.dropFirst()
    }
