    /// Generate a SKILL.md for the META monitor agent.
    ///
    /// - Parameter cop: The Cockpit Orchestration Plan
    /// - Returns: Markdown skill definition
    func generateMetaSkill(cop: CockpitOrchestrationPlan) -> String {
        let riskAreas = cop.specialistTriggers.map(\.specialist)
        let riskAreasStr = riskAreas.isEmpty ? "none identified" : riskAreas.joined(separator: ", ")

        let testCmd: String
        switch cop.projectType {
        case "cli", "library":
            testCmd = "cargo test"
        default:
            testCmd = "npm test"
        }

        let capabilitiesList = cop.metaAgentConfig.capabilities
            .map { "- \($0)" }
            .joined(separator: "\n")

        return """
        # META Monitor Skill

        You are the code quality monitor for the **\(cop.projectName)** project.

        ## Your responsibilities:
        - Monitor all git worktrees for code quality issues
        - Run `\(testCmd)` to verify test coverage after each agent iteration
        - Check for security vulnerabilities in new code
        - Auto-generate documentation updates when APIs change
        - Flag merge conflicts early and report to the cockpit
        - Report status every iteration

        ## Project context:
        - **Type:** \(cop.projectType)
        - **Domain:** \(cop.domain)
        - **Market:** \(cop.marketContext)
        - **Risk areas:** \(riskAreasStr)

        ## Capabilities:
        \(capabilitiesList)

        ## You have access to:
        - Git operations (diff, log, merge, worktree list)
        - Test runners (`\(testCmd)`)
        - File system (read/write .md files in .crossroads/)
        - Conflict detection between worktrees

        ## Workflow:
        1. Check each active worktree for uncommitted changes
        2. Run tests in each worktree
        3. Compare diffs between worktrees for potential conflicts
        4. Update `.crossroads/meta-status.json` with findings
        5. Create alerts for any issues found
        """
    }

    /// Generate a SKILL.md for a transverse production agent.
    ///
    /// - Parameters:
