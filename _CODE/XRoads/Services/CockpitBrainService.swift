    ///   - category: The transverse category name
    /// - Returns: Markdown skill definition
    func generateTransverseSkill(cop: CockpitOrchestrationPlan, category: String) throws -> String {
        guard let transverse = cop.transverseProductions.first(where: { $0.category == category }) else {
            throw CockpitBrainError.transverseCategoryNotFound(category)
        }

        let deliverablesList = transverse.deliverables
            .map { "- [ ] `\($0).md`" }
            .joined(separator: "\n")

        let specialistContext: String
        if cop.specialistTriggers.isEmpty {
            specialistContext = "No specialist concerns identified."
        } else {
            specialistContext = cop.specialistTriggers
                .map { "- **\($0.specialist)**: \($0.reason)" }
                .joined(separator: "\n")
        }

        let categoryTitle = capitalizeFirst(category)

        return """
        # \(categoryTitle) Production Skill

        You are producing **\(category)** deliverables for the **\(cop.projectName)** project.

        ## Deliverables to create:
        \(deliverablesList)

        ## Quality requirements:
        - Each deliverable must be a standalone `.md` file
        - Each deliverable must be actionable and specific (no filler content)
        - Reference actual project context, features, and domain terminology
        - Save all files to: `.crossroads/deliverables/\(category)/`

        ## Project context:
        - **Project:** \(cop.projectName)
        - **Type:** \(cop.projectType)
        - **Domain:** \(cop.domain)
        - **Market:** \(cop.marketContext)
        - **Priority for this category:** \(transverse.priority)

        ## Specialist considerations:
        \(specialistContext)

        ## Workflow:
        1. Read the project README and PRD to understand features
        2. Create each deliverable file in order of importance
        3. Cross-reference deliverables for consistency
        4. Write a `_index.md` summarizing all deliverables produced
        """
    }

